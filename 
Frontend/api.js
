
// API Utils for Backend Integration
const API_BASE = 'http://localhost:5000/api';

window.api = {
  // Login validation + API call
  async login(email, password) {
    // Client-side validation
    if (!email || !password) throw new Error('Email and password required');
    if (!email.includes('@')) throw new Error('Valid email required');
    if (password.length < 6) throw new Error('Password min 6 chars');
    
    try {
      const response = await fetch(`${API_BASE}/users/login`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ email, password })
      });
      
      const data = await response.json();
      if (!response.ok || !data.success) throw new Error(data.error || 'Login failed');
      
      localStorage.setItem('token', data.token);
      localStorage.setItem('userRole', data.user.role);
      return data.user;
    } catch (error) {
      throw new Error('Login error: ' + error.message);
    }
  },
  
  // Protected fetch wrapper
  async fetch(endpoint, options = {}) {
    const token = localStorage.getItem('token');
    if (!token) throw new Error('Not authenticated');
    
    const headers = {
      'Content-Type': 'application/json',
      'Authorization': `Bearer ${token}`,
      ...options.headers
    };
    
    const response = await fetch(`${API_BASE}${endpoint}`, { ...options, headers });
    const data = await response.json();
    
    if (!response.ok) {
      if (response.status === 401) {
        localStorage.clear();
        window.location.href = 'login.html';
        throw new Error('Session expired');
      }
      throw new Error(data.error || 'Request failed');
    }
    
    return data;
  },
  
  logout() {
    localStorage.clear();
    window.location.href = 'login.html';
  },
  
  getCurrentUser() {
    return this.fetch('/users/me');
  },
  
  getAllUsers() {
    return this.fetch('/users/users');
  }
};

